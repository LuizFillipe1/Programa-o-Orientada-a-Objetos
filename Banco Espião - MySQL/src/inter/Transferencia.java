    private void TransferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TransferirActionPerformed
        // TODO add your handling code here:
        if(ValorTransferencia.getText() != ""){
            if (ContaAtual2.getSaldo() + ContaAtual2.getLimite() >= Double.parseDouble(ValorTransferencia.getText())){
                java.util.Date dataAtual = new Date();
                String dataFormatada = java.text.DateFormat.getDateInstance(DateFormat.MEDIUM).format(dataAtual);
                ContaAtual2.Sacar(2, "TED Enviado", dataFormatada, Double.parseDouble(ValorTransferencia.getText()));
                JOptionPane.showMessageDialog(null,"Transferencia Realizada com Sucesso!","Informação",INFORMATION_MESSAGE);
                ContaAtual2.imprimirDados();
                setVisible(false);
                dispose();
                //ContaAtual.Depositar(1, "TED Recebido", dataFormatada, Double.parseDouble(ValorTransferencia.getText()));
            }
            else
            JOptionPane.showMessageDialog(null,"Saldo insuficiente!","Informação",INFORMATION_MESSAGE);

        }
        else
        JOptionPane.showMessageDialog(null,"Informe o valor de deposito","Informação",INFORMATION_MESSAGE);
    }//GEN-LAST:event_TransferirActionPerformed

