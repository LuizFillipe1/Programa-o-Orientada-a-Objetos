    private void Buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Buscar1ActionPerformed
        // TODO add your handling code here:
        boolean encontrou = false;  //Flag para garantir se encontrou ou não a conta buscada
        int tamanho = Inter.ListaContaCorrente.size();
        for (int k = 0; k < tamanho; k++){
            ContaAtual2 = Inter.ListaContaCorrente.get(k);
            if (ContaAtual2.getAgencia() == Integer.parseInt(AgenciaOrigem.getText()) &&
                ContaAtual2.getConta() == Integer.parseInt(ContaOrigem.getText())) {
                LabelAgenciaDestino.setEnabled(true);
                LabelContaDestino.setEnabled(true);
                AgenciaDestino.setEnabled(true);
                ContaDestino.setEnabled(true);
                Buscar2.setEnabled(true);
                
                encontrou = true;
                break; //isso de nodo a parar no objeto atual
            }
        }
        if(!encontrou){
            JOptionPane.showMessageDialog(null,"Conta não encontrada","Informação",INFORMATION_MESSAGE);   JOptionPane.showMessageDialog(null,"Conta não encontrada","Informação",INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_Buscar1ActionPerformed

